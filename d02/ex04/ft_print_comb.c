void	ft_print_comb(void);

void	ft_print_comb(void)
{
	char ch[3];

	ch[0] = '0' - 1;
	while (++ch[0] <= '9')
	{
		ch[1] = ch[0];
		while (++ch[1] <= '9')
		{
			ch[2] = ch[1];
			while (++ch[2] <= '9')
			{
				if (ch[0] != 0 || ch[1] != 1 || ch[2] != 2)
					ft_putchar(',');
				if (ch[0] != 0 || ch[1] != 1 || ch[2] != 2)
					ft_putchar(' ');
				ft_putchar(ch[0]);
				ft_putchar(ch[1]);
				ft_putchar(ch[2]);
			}
		}
	}
}
